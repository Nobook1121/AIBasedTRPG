import { mkdir, readFile, readdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");

async function readJson(relativePath) {
  const source = await readFile(path.join(root, relativePath), "utf8");
  return JSON.parse(source.replace(/^\uFEFF/, ""));
}

async function writeConcatenatedFile({ manifestPath, sourceDir, outputPath }) {
  const parts = await readJson(manifestPath);
  const chunks = await Promise.all(
    parts.map(async (part) => {
      const source = await readFile(path.join(root, sourceDir, part), "utf8");
      return source.replace(/^\uFEFF/, "");
    }),
  );
  const destination = path.join(root, outputPath);
  await mkdir(path.dirname(destination), { recursive: true });
  await writeFile(destination, chunks.join("\n"), "utf8");
}

async function listHtmlFiles(directory) {
  const entries = await readdir(directory, { withFileTypes: true });
  const nested = await Promise.all(entries.map(async (entry) => {
    const fullPath = path.join(directory, entry.name);
    if (entry.isDirectory()) return listHtmlFiles(fullPath);
    if (entry.isFile() && entry.name.endsWith(".html")) return [fullPath];
    return [];
  }));
  return nested.flat().sort();
}

function extractTemplates(filePath, source) {
  const templates = [];
  const pattern = /<template\s+id="([^"]+)"\s*>([\s\S]*?)<\/template>/g;
  for (const match of source.matchAll(pattern)) {
    templates.push({
      id: match[1],
      html: match[2].trim(),
      filePath,
    });
  }
  return templates;
}

async function generateTemplates() {
  const templateDir = path.join(root, "frontend", "src", "templates");
  const files = await listHtmlFiles(templateDir);
  const templates = [];

  for (const file of files) {
    templates.push(...extractTemplates(file, await readFile(file, "utf8")));
  }

  const ids = new Set();
  for (const template of templates) {
    if (ids.has(template.id)) {
      throw new Error(`Duplicate template id: ${template.id}`);
    }
    ids.add(template.id);
  }

  const templateObject = Object.fromEntries(templates.map((template) => [template.id, template.html]));
  const generated = `// This file is generated by scripts/build-frontend.mjs. Do not edit by hand.
const TrpgTemplates = (() => {
    const templates: Record<string, string> = ${JSON.stringify(templateObject, null, 4)};

    function escapeHtml(value: unknown): string {
        return String(value ?? "")
            .replace(/&/g, "&amp;")
            .replace(/</g, "&lt;")
            .replace(/>/g, "&gt;")
            .replace(/"/g, "&quot;")
            .replace(/'/g, "&#039;");
    }

    function render(templateId: string, values: Record<string, unknown> = {}): string {
        const template = templates[templateId];
        if (template === undefined) {
            throw new Error(\`Unknown frontend template: \${templateId}\`);
        }
        return template
            .replace(/\\{\\{\\{\\s*([\\w.]+)\\s*\\}\\}\\}/g, (_, key: string) => String(values[key] ?? ""))
            .replace(/\\{\\{\\s*([\\w.]+)\\s*\\}\\}/g, (_, key: string) => escapeHtml(values[key]));
    }

    return { render, escapeHtml };
})();

window.TrpgTemplates = TrpgTemplates;
`;

  const outputDir = path.join(root, "frontend", "src", "js", "generated");
  await mkdir(outputDir, { recursive: true });
  await writeFile(path.join(outputDir, "templates.ts"), generated, "utf8");
}

async function main() {
  await writeConcatenatedFile({
    manifestPath: "frontend/src/index/index.parts.json",
    sourceDir: "frontend/src/index",
    outputPath: "frontend/dist/index.html",
  });
  await generateTemplates();
}

await main();
